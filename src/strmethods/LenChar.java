package strmethods;

public class LenChar {

	public static void main(String[] args) {
		 String str="hyderabad";
		    int len=str.length();
		    System.out.println(len);
		    for (int i=0;i<len;i++) {
		    	char ch=str.charAt(i);
		    	System.out.print(ch);
		    }
			}

	}


