package strmethods;

public class IndexofStr {

	public static void main(String[] args) {
		String str="elamanchili";
		int p1=str.indexOf('e');
		int p2=str.indexOf('a');
		int p3=str.indexOf('a',4);
		int p4=str.indexOf('i');
		int p5=str.indexOf('i',10);
		int p6=str.indexOf('z');
		int p7=str.indexOf(4);
		System.out.print(p1);
		System.out.print(p2);
		System.out.print(p3);
		System.out.print(p4);
		System.out.print(p5);
		System.out.print(p6);
		System.out.println(p7);
		

	}

}
